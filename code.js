var run_sim = function(){
  cy.nodes().forEach(function( ele ){//Pull each node
    var output_line= 1;
    var Excitor = 1;
    var Inhibitor = -1;
    if(ele.data('weight')==0){//Excitor
              output_line = Excitor;
    }
    else {//Inhibitor
        output_line = Inhibitor;
    }
    var node_sum=0
    ele.incomers().forEach(function (foo){//sum the incoming edge weights
          node_sum+=parseInt(foo.data('weight'));
          console.log("adding node weights");
    });
    if(node_sum>=3){
      console.log("checking node weights");
      ele.outgoers().forEach(function (bar){//set output weights
        bar.data('weight',output_line);
        if(output_line ==Excitor){
          //ele.classes();
          //bar.classes();
          ele.toggleClass('highlighted',false);
          ele.toggleClass('highlighted',true);
          ele.toggleClass('highlighted',false);
          ele.toggleClass('highlighted',true);
          //bar.addClass('highlighted');
          console.log(ele);
        //  console.log("adding classes");
        }
        else {
      //    ele.classes();
        //  bar.classes();
          ele.addClass('inhibitorhighlighted');
          bar.addClass('inhibitorhighlighted');
        }
      //  console.log(ele.style());
      });
    }
  });

};
